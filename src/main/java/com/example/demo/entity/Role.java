package com.example.demo.entity;

@Entity

public class Role {
    @Id
    private String userId;
    @Id
    private String role;

    // Getters and Setters
    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
